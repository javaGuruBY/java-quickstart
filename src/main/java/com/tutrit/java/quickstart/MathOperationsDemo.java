package com.tutrit.java.quickstart;

public class MathOperationsDemo {
    public static void mathDemo() {

        int a = 2;
        int b = 5;
        //сложение
        System.out.println(a + "+" + b + "=" + (a + b));
        //вычитание
        System.out.println(a + "-" + b + "=" + (a - b));
        //умножение
        System.out.println(a + "*" + b + "=" + (a * b));
        //деление
        System.out.println(a + "/" + b + "=" + (a / b));


    }
}
